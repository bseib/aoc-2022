class Problem2 : DailyProblem {

    class Weapon(moniker: String) {
        private val value: Int = when (moniker) {
            "A" -> 1 // Rock
            "B" -> 2 // Paper
            "C" -> 3 // Scissors
            else -> throw Exception("invalid moniker")
        }
        private val flipValue  // translate values of { 1, 2, 3 } => { 3, 2, 1 }
            get() = 1 + ((value * 2) % 3)

        //        X=1  Y=2  Z=3  <== me
        //  A=3 |  3    6    0
        //  B=2 |  0    3    6     ((me + them) % 3) * 3
        //  C=1 |  6    0    3
        //  them (flipped to descending 3, 2, 1 values)
        fun battle(opponentWeapon: Weapon) = value + (((value + opponentWeapon.flipValue) % 3) * 3)

        //         1    2    3  <== desired outcome { 1=lose, 2=draw, 3=win }
        //  A=1 | C=3  A=1  B=2
        //  B=2 | A=1  B=2  C=3    ((desired + them) % 3) + 1
        //  C=3 | B=2  C=3  A=1
        //  them
        fun chooseOpponentWeapon(desiredOutcome: Int) = Weapon(listOf("A", "B", "C")[(desiredOutcome + value) % 3])
    }

    val naiveWeaponPicker = { xyz: String, abc: String ->
        Weapon(
            when (xyz) {
                "X" -> "A"
                "Y" -> "B"
                "Z" -> "C"
                else -> throw Exception("Expected X, Y, or Z")
            }
        )
    }

    val realWeaponPicker = { xyz: String, abc: String ->
        Weapon(abc).chooseOpponentWeapon(
            when (xyz) {
                "X" -> 1
                "Y" -> 2
                "Z" -> 3
                else -> throw Exception("Expected X, Y, or Z")
            }
        )
    }

    fun solvePart0(): Int {
        return toWeaponPairList(data0, naiveWeaponPicker).map { it.second.battle(it.first) }.sum()
            .also { println("battle0 = $it") }
    }

    override fun solvePart1(): Int {
        return toWeaponPairList(data1, naiveWeaponPicker).map { it.second.battle(it.first) }.sum()
            .also { println("battle1 = $it") }
    }

    override fun solvePart2(): Int {
        return toWeaponPairList(data1, realWeaponPicker).map { it.second.battle(it.first) }.sum()
            .also { println("battle2 = $it") }
    }

    private fun toWeaponPairList(
        data: List<String>,
        pick: (xyz: String, abc: String) -> Weapon,
    ): List<Pair<Weapon, Weapon>> {
        return data.map { it.trim() }
            .map { it.split(" ") }
            .map { Pair(Weapon(it[0]), pick(it[1], it[0])) } // them, us
    }

